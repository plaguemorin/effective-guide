void print_map(const Map& map, const thing::Manager& manager) {
  for (int y = 0; y < map.height(); y++) {
    for (int x = 0; x < map.width(); x++) {
      char modStart = ' ';
      char modEnd = ' ';

      if (auto thing = manager.at(x, y)) {
        std::tie(modStart, modEnd) = thing->with_component<TextPrintComponent>(
                [](const TextPrintComponent& component) {
                  return std::make_pair(component.begin(), component.end());
                },
                std::make_pair(' ', ' ')
        );
      }

      printf("%c%04X%c",
             modStart,
             map.info_at_tile(x, y).tile_id,
             modEnd);
    }
    printf(" |\n");
  }
}

